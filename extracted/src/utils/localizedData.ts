// Fallback logic helpers
export function getLProduct(product: Product, lang: string): Product {
  const trans = LOCALIZED_PRODUCTS[lang]?.[product.id];
  if (!trans) return product;
  
  return {
    ...product,
    name: trans.name || product.name,
    shortDescription: trans.shortDescription || product.shortDescription,
    fullDescription: trans.fullDescription || product.fullDescription,
    season: trans.season || product.season,
    specs: {
      ...product.specs,
      ...(trans.specs || {})
    }
  };
}

export function getLBlog(blog: BlogArticle, lang: string): BlogArticle {
  const trans = LOCALIZED_BLOGS[lang]?.[blog.id];
  if (!trans) return blog;
  
  return {
    ...blog,
    title: trans.title || blog.title,
    summary: trans.summary || blog.summary,
    content: trans.content || blog.content
  };
}
