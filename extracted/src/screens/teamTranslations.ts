export function translateCropSpecialty(text: string, lang: string): string {
  if (!text) return "";
  
  // Decide dictionary based on language
  let activeMap = CROP_MAP_FA;
  if (lang === "fa" || lang === "ar") {
    activeMap = CROP_MAP_FA;
  } else if (lang === "ru" || lang === "uk") {
    activeMap = CROP_MAP_RU;
  } else {
    // Other languages keep English crop specialties or slight fallback
    return text;
  }

  // Pre-clean text split by comma and translate fragments for extremely beautiful natural result
  const fragments = text.split(",").map(s => s.trim());
  const translatedFragments = fragments.map(frag => {
    // If the whole fragment is mapped, return it
    const lowerFrag = frag.toLowerCase();
    if (activeMap[lowerFrag]) return activeMap[lowerFrag];

    // Otherwise split into words and replace
    const words = frag.split(" ");
    const translatedWords = words.map(word => {
      const cleanWord = word.toLowerCase().replace(/[^a-z'-]/g, "");
      if (activeMap[cleanWord]) {
        // preserve spacing or symbols
        return activeMap[cleanWord];
      }
      return word;
    });

    if (lang === "fa" || lang === "ar") {
      return translatedWords.join(" و ");
    }
    return translatedWords.join(" ");
  });

  if (lang === "fa" || lang === "ar") {
    return translatedFragments.join("، ");
  }
  return translatedFragments.join(", ");
}

export function translateLanguages(langs: string[], lang: string): string[] {
  if (!langs) return [];
  const currentTexts = TEAM_TEXTS[lang] || TEAM_TEXTS.en;
  return langs.map(l => {
    return currentTexts.languages[l] || l;
  });
}

